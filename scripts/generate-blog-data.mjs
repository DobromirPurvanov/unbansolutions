import { mkdir, readFile, readdir, writeFile } from 'node:fs/promises';
import path from 'node:path';
import matter from 'gray-matter';
import { marked } from 'marked';

const root = process.cwd();
const blogDirectory = path.join(root, 'public', 'blog');
const output = path.join(root, 'src', 'generated', 'blog-posts.ts');

const renderer = new marked.Renderer();
renderer.html = ({ text }) => String(text)
  .replaceAll('&', '&amp;')
  .replaceAll('<', '&lt;')
  .replaceAll('>', '&gt;');

const posts = [];
function toMetaDescription(value) {
  let description = String(value).trim();
  if (description.length < 135) description += ' Практически насоки от Unban Solutions.';
  if (description.length > 160) description = `${description.slice(0, 157).trimEnd()}…`;
  return description;
}

for (const filename of await readdir(blogDirectory)) {
  if (!filename.endsWith('.md')) continue;
  const raw = await readFile(path.join(blogDirectory, filename), 'utf8');
  const { data, content } = matter(raw);
  const slug = filename.slice(0, -3);
  const articleContent = content.replace(/^\s*#\s+[^\r\n]+(?:\r?\n)*/, '');
  const words = content.trim().split(/\s+/).filter(Boolean).length;
  const excerpt = data.excerpt || `${content.slice(0, 150)}…`;
  posts.push({
    slug,
    title: data.title || slug,
    excerpt,
    metaDescription: toMetaDescription(excerpt),
    date: data.date || new Date().toISOString().slice(0, 10),
    readTime: data.readTime || `${Math.max(1, Math.ceil(words / 200))} мин`,
    content,
    html: await marked.parse(articleContent, { renderer }),
    coverImage: data.coverImage,
    tags: Array.isArray(data.tags) ? data.tags : [],
  });
}

posts.sort((a, b) => new Date(b.date).getTime() - new Date(a.date).getTime());
const source = `// Generated by scripts/generate-blog-data.mjs. Do not edit directly.\nimport type { BlogPost } from '@/lib/blog';\n\nexport const blogPosts: BlogPost[] = ${JSON.stringify(posts, null, 2).replaceAll('<', '\\u003c')};\n`;
await mkdir(path.dirname(output), { recursive: true });
await writeFile(output, source, 'utf8');
console.log(`Generated browser-safe data for ${posts.length} blog posts.`);
